    private void llenarComboBox() {
        try {
            GestorParqueadero gestor = new GestorParqueadero();
            List<Parqueadero> lista = gestor.getParqueaderos(usuario.getAtrCedula());
            for(Parqueadero parqueadero : lista) {
                cbParqueaderos.addItem(parqueadero.getNombre());
            }
        } catch (Exception e) {
        }

    }

